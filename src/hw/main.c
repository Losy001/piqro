#include <third_party/font8x8_basic.h>

static void text(int16_t x, int16_t y, int16_t s, String text)
{
	uint16_t px = 0;
	uint16_t py = 0;

	for (size_t i = 0; i < text.length; i++) 
	{
		if (text.buffer[i] == ' ')
		{
			px++;
			continue;
		}

		if (text.buffer[i] == '\t')
		{
			px += 3;
			continue;
		}

		if (text.buffer[i] == '\n')
		{
			px = 0;
			py++;

			continue;
		}

		for (uint8_t tx = 0; tx < 8; tx++)
		{
			for (uint8_t ty = 0; ty < 8; ty++)
			{
				if (!((font8x8_basic[text.buffer[i]][ty] >> tx) & 1))
				{
					continue;
				}

				lcd_fill_rect(x + (px * 8 * s) + (tx * s), y + (py * 8 * s) + (ty * s), s, s, 0xffff);
			}
		}

		px++;
	}
}

static bool had_error = false;

static void vm_error_fn(const char* what)
{
	if (had_error)
	{
		return;
	}
	
	had_error = true;
	
	String s = {};

	s.buffer = what;
	while (*what++) s.length++;

	text(0, 0, 1, s);
}

void rt_print(const char*) {}

static constexpr uint8_t PIN_LEFT_KEY = 2;
static constexpr uint8_t PIN_RIGHT_KEY = 3;
static constexpr uint8_t PIN_UP_KEY = 4;
static constexpr uint8_t PIN_DOWN_KEY = 5;
static constexpr uint8_t PIN_A_KEY = 6;
static constexpr uint8_t PIN_B_KEY = 7;

static RT_State rt;

void key_callback(unsigned int gpio, long unsigned int event_mask)
{
	// With pull-ups: button pressed = LOW, released = HIGH
	// EDGE_FALL = button pressed, EDGE_RISE = button released
	
	bool is_pressed = (event_mask & GPIO_IRQ_EDGE_FALL);
	
	if (gpio == PIN_LEFT_KEY)
	{
		rt.left_key = is_pressed;
	}
	else if (gpio == PIN_RIGHT_KEY)
	{
		rt.right_key = is_pressed;
	}
	else if (gpio == PIN_UP_KEY)
	{
		rt.up_key = is_pressed;
	}
	else if (gpio == PIN_DOWN_KEY)
	{
		rt.down_key = is_pressed;
	}
	else if (gpio == PIN_A_KEY)
	{
		rt.a_key = is_pressed;
	}
	else if (gpio == PIN_B_KEY)
	{
		rt.b_key = is_pressed;
	}
}

int main()
{		
	stdio_init_all();

  // Initialize all button pins
  gpio_init(PIN_LEFT_KEY);
  gpio_init(PIN_RIGHT_KEY);
  gpio_init(PIN_UP_KEY);
  gpio_init(PIN_DOWN_KEY);
  gpio_init(PIN_A_KEY);
  gpio_init(PIN_B_KEY);
  
  // Set all as inputs
  gpio_set_dir(PIN_LEFT_KEY, GPIO_IN);
  gpio_set_dir(PIN_RIGHT_KEY, GPIO_IN);
  gpio_set_dir(PIN_UP_KEY, GPIO_IN);
  gpio_set_dir(PIN_DOWN_KEY, GPIO_IN);
  gpio_set_dir(PIN_A_KEY, GPIO_IN);
  gpio_set_dir(PIN_B_KEY, GPIO_IN);
  
  // Enable pull-ups (assumes buttons connect pin to GND when pressed)
  gpio_pull_up(PIN_LEFT_KEY);
  gpio_pull_up(PIN_RIGHT_KEY);
  gpio_pull_up(PIN_UP_KEY);
  gpio_pull_up(PIN_DOWN_KEY);
  gpio_pull_up(PIN_A_KEY);
  gpio_pull_up(PIN_B_KEY);

	// Use EDGE interrupts instead of LEVEL interrupts
	// Set callback for the first pin
	gpio_set_irq_enabled_with_callback(PIN_LEFT_KEY, GPIO_IRQ_EDGE_FALL | GPIO_IRQ_EDGE_RISE, true, key_callback);

	// Enable interrupts for remaining pins (callback already set)
	gpio_set_irq_enabled(PIN_RIGHT_KEY, GPIO_IRQ_EDGE_FALL | GPIO_IRQ_EDGE_RISE, true);
	gpio_set_irq_enabled(PIN_UP_KEY, GPIO_IRQ_EDGE_FALL | GPIO_IRQ_EDGE_RISE, true);
	gpio_set_irq_enabled(PIN_DOWN_KEY, GPIO_IRQ_EDGE_FALL | GPIO_IRQ_EDGE_RISE, true);
	gpio_set_irq_enabled(PIN_A_KEY, GPIO_IRQ_EDGE_FALL | GPIO_IRQ_EDGE_RISE, true);
	gpio_set_irq_enabled(PIN_B_KEY, GPIO_IRQ_EDGE_FALL | GPIO_IRQ_EDGE_RISE, true);

	lcd_set_pins(16, 18, 17, 10, 11);
	lcd_init();
	lcd_set_rotation(1);
	
	lcd_fill_screen(0x0000);
	
	static uint8_t mem[64 * 1024];
	Arena arena = arena_make(mem, sizeof(mem));
		
	rt_state_init(&arena, &rt);

	PQ_CompiledBlob blob = {}; 
	
	blob.buffer = program;
	blob.size = sizeof(program);
	
	PQ_VM vm = {};
	
	pq_vm_init(&vm, &arena, &blob, vm_error_fn);
	rt_bind_procedures(&vm);

	while (!had_error && pq_execute(&vm)) 
	{

	}
}

